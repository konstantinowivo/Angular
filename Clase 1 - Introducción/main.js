// Que es Angular?

// Angular es un framework para crear aplicaciones web SPA(Simple Page Aplication)
// La caracteristicas de estas páginas es que son de una sóla página, al contrario de un sitio web
// que se compone de muchas páginas. Esto no quiere decir que sea más básica ni menos funcional


// Quien creó Angular?
// Google



// Que caracteristicas tiene?

// * Separa frontend de backend de la aplicación
// * simplifica el código
// * sigue el patrón MVC (Modelo Vista Controlador)
// * Basado en componentes
// * Es de código abierto


// Que lenguaje utiliza?

// Typescript (Basado en JS, pero con mayor orientación a los objetos)

// Por que utilizar Angular?

// * El desarrollo de aplicaciones es rápido y la navegación también mediante AJAX(asincronía)
// * Es modular (reutilizable) gracias a MVC, cosas que desarrollamos para una aplicacion
// podemos usarlas en otras
// * Fácil mantenimiento
// * Es multiplataforma
// * Futuro estable
// * Gran soporte de herramientas
// * Creciente demanda